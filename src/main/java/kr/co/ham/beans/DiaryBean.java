package kr.co.ham.beans;

public class DiaryBean {

		private int dia_key;
		private int dia_walk;
		private int dia_weather;
		private int dia_writer_index;
		private String dia_date;
		private String dia_title;
		private String dia_content;
		
		public int getDia_key() {
			return dia_key;
		}
		public void setDia_key(int dia_key) {
			this.dia_key = dia_key;
		}
		public int getDia_walk() {
			return dia_walk;
		}
		public void setDia_walk(int dia_walk) {
			this.dia_walk = dia_walk;
		}
		public int getDia_weather() {
			return dia_weather;
		}
		public void setDia_weather(int dia_weather) {
			this.dia_weather = dia_weather;
		}
		
		public int getDia_writer_index() {
			return dia_writer_index;
		}
		public void setDia_writer_index(int dia_writer_index) {
			this.dia_writer_index = dia_writer_index;
		}
		public String getDia_date() {
			return dia_date;
		}
		public void setDia_date(String dia_date) {
			this.dia_date = dia_date;
		}
		public String getDia_title() {
			return dia_title;
		}
		public void setDia_title(String dia_title) {
			this.dia_title = dia_title;
		}
		public String getDia_content() {
			return dia_content;
		}
		public void setDia_content(String dia_content) {
			this.dia_content = dia_content;
		}
		
}
